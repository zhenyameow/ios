/*:
 ## Упражнение - порядок операций

 Напечатайте, чему, по вашему мнению, будет равно 10 + 2 * 5.  Затем напечатайте само выражение (т.е. `print(10 + 2 * 5)`)
 */
print("10 + 2 * 5 = 20")
