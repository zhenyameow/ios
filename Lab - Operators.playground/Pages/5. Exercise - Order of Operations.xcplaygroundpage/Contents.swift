/*:
 В новом операторе `print` добавьте необходимые скобки, чтобы сложение произошло перед умножением.
 */
print((10 + 2) * 5)

/*:
 Напечатайте, чему, по вашему мнению, будет равно 4 * 9 - 6 / 2.  Затем напечатайте само выражение.
 */
print("4 * 9 - 6 / 2 = 33")
print(4 * 9 - 6 / 2)
/*:
 В ещё одном операторе `print` добавьте скобки так, чтобы вычитание получило приоритет перед умножением и делением.
